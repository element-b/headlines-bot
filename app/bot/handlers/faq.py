from __future__ import annotations

from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router(name="faq")


FAQ_TEXT = (
    "❓ <b>FAQ — как работает бот</b>\n\n"
    "Этот бот собирает заголовки новостей из нескольких источников, сохраняет их в базу данных "
    "и позволяет получать новости по запросу или по подписке.\n\n"
    "1. <b>Чем отличается источник по умолчанию от подписки?</b>\n"
    "• Источник по умолчанию используется в команде /news.\n"
    "• Подписки нужны для автоматических уведомлений о новых новостях.\n\n"
    "2. <b>Что делает команда /news?</b>\n"
    "Показывает последние новости из источника по умолчанию. "
    "Если источник по умолчанию ещё не выбран, бот предложит выбрать источник вручную.\n\n"
    "3. <b>Что делает команда /news_all?</b>\n"
    "Показывает последние новости из всех доступных источников.\n\n"
    "4. <b>Сколько заголовков присылает бот?</b>\n"
    "Количество заголовков задаётся в /settings. "
    "Эта настройка влияет на выдачу новостей по запросу.\n\n"
    "5. <b>Что приходит по подписке?</b>\n"
    "По подписке приходят только новые заголовки, которые появились после активации подписки. "
    "Старые новости сразу после подписки не отправляются.\n\n"
    "6. <b>Где изменить настройки?</b>\n"
    "В /settings можно выбрать источник по умолчанию и количество заголовков.\n\n"
    "7. <b>Как управлять подписками?</b>\n"
    "• /subscribe — добавить подписки\n"
    "• /mysubs — посмотреть активные подписки\n"
    "• /unsubscribe — отключить подписку\n\n"
    "Если хочешь быстро начать, используй команды:\n"
    "• /settings\n"
    "• /news\n"
    "• /subscribe"
)


@router.message(Command("faq"))
async def faq_command(message: Message) -> None:
    """Handle /faq command."""
    await message.answer(FAQ_TEXT, disable_web_page_preview=True)